# Criando uma tupla com um único item
tupla_unica = (42,)  # A vírgula é essencial

# Criando uma tupla usando tuple()
tupla_unica_2 = tuple([100])  # Convertendo uma lista com um único item para tupla

# Imprimindo as tuplas
print("Tupla única 1:", tupla_unica)
print("Tupla única 2:", tupla_unica_2)

# Verificando os tipos
print("Tipo de tupla_unica:", type(tupla_unica))
print("Tipo de tupla_unica_2:", type(tupla_unica_2))

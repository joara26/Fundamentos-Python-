#Desenvolva um programa que apresente se um numero introduzido pelo utilizador é par ou impar
#se o numero for par a mensagem deve dizer que é par
#se for impar a mensagem deve dizer que o numero é impar
n = int(input( 'introduza um numero:'))
if n % 2 == 0: print('o numero e par')
else: print ('o numero e impar')
